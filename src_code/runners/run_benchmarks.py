def run_family(
    family: str,
    N_list: List[int],
    seeds: List[int],
    skip_existing: bool,
    verbose: bool,
    gen_kwargs: Optional[Dict] = None,
    echo_debug: bool = False,
) -> None:
    """
    Full SA + SA-ECHO benchmark pipeline for one family.
    Progress bar advances after every individual solver run (SA or ECHO),
    so each step is visible and the bar never appears frozen.
    CSV files are written immediately after each solver completes so
    results can be monitored in real time.
    """
    gen_kwargs  = gen_kwargs or FAMILY_GEN_KWARGS.get(family, {})

    # IMPORTANT (resume-safe):
    # Only archive existing results when the user explicitly requests a fresh rerun.
    # If skip_existing=True, the user is resuming; archiving would delete the very
    # files needed to detect and skip completed runs.
    if not skip_existing:
        _archive_results(family)

    results_dir = _ROOT / "results" / family
    sa_csv      = results_dir / f"{family}_sa_results.csv"
    echo_csv    = results_dir / f"{family}_echo_results.csv"
    master_csv  = results_dir / f"{family}_results_master.csv"

    instances = [(N, seed) for N in N_list for seed in seeds]
    n_inst    = len(instances)
    # Each instance has 2 solver steps (SA + ECHO); bar advances on each
    n_steps   = n_inst * 2

    print(f"\n{'='*60}")
    print(f"  Benchmark : {family}")
    print(f"  N values  : {N_list}")
    print(f"  Seeds     : {seeds[0]}..{seeds[-1]}  ({len(seeds)} seeds)")
    print(f"  Instances : {n_inst}  (SA + ECHO-SA per instance = {n_steps} steps)")
    print(f"  Mode      : {'skip existing (resume-safe)' if skip_existing else 'force rerun'}")
    print(f"  Output    : results/{family}/{family}_sa_results.csv  (written after every SA)")
    print(f"              results/{family}/{family}_echo_results.csv  (written after every ECHO-SA)")
    print(f"              results/{family}/{family}_results_master.csv  (rebuilt at end)")
    # Rough timing guidance so the user knows what to expect
    print(f"  Estimated : ~0.3-5s per step depending on N  "
          f"(N=50 ≈ 0.3s, N=300 ≈ 5s per solver)")
    print(f"{'='*60}", flush=True)

    # Create the output directory and empty CSV files with headers immediately,
    # before preflight. This means the folder and files are visible from the
    # very start of the run — not only after the first result row is written.
    results_dir.mkdir(parents=True, exist_ok=True)
    for csv_path in (sa_csv, echo_csv):
        if not csv_path.exists():
            pd.DataFrame(columns=_BASE_COLS).to_csv(csv_path, index=False)
    print(
        f"  Created results/{family}/  "
        f"({family}_sa_results.csv + {family}_echo_results.csv ready)"
    )

    # Load done-keys once (fast resume-safe skipping).
    # We also keep these sets updated as we append rows during the run.
    sa_done_keys   = _load_done_keys(sa_csv)
    echo_done_keys = _load_done_keys(echo_csv)

    # --- Pre-flight sanity checks (abort before wasting time if broken) ---
    try:
        _run_preflight(family, gen_kwargs)
    except PreflightError as exc:
        print(f"\n{'!'*60}")
        print(str(exc))
        print(f"{'!'*60}")
        print("  Aborting. Fix the issue and re-run.")
        return

    try:
        from tqdm import tqdm
        _tqdm = tqdm
    except ImportError:
        _tqdm = None

    completed_sa   = 0
    completed_echo = 0
    skipped        = 0

    if _tqdm is not None:
        bar = _tqdm(
            total=n_steps,
            desc=f"  {family}",
            unit="step",
            ncols=72,
            leave=True,    # keep completed bar on screen; next family starts fresh line
        )
    else:
        bar = None

    def _bar_update(label: str) -> None:
        if bar is not None:
            bar.set_postfix_str(label, refresh=True)
            bar.update(1)
        else:
            print(f"  {label}", flush=True)

    for N, seed in instances:
        # ---- Generate QUBO (fast, no bar step) ----
        inst   = generate_instance(family, N, seed, **gen_kwargs)
        Q_base = inst["Q_base"]
        Q_pen  = inst["Q_pen"]
        w_pen  = inst["w_pen"]
        meta   = inst["meta"]
        Q_full = Q_base + w_pen * Q_pen

        # spectral_dense carries its own instance_id (includes kappa_tag);
        # other families use the generic pattern.
        instance_id = meta.get("instance_id") or f"{family}_N{N}_seed{seed}"

        # maxcut and spectral_dense: Q_pen is a pure smoothing matrix (ECHO only).
        # Adding it to Q_eval turns the indefinite MaxCut Laplacian into a PSD
        # matrix whose global minimum is v=0 with E=0 — both SA and ECHO return
        # the trivial zero vector.  Evaluate on Q_base (the true target QUBO).
        # portfolio_card: Q_pen encodes the cardinality penalty and IS part of
        # the objective, so Q_eval = Q_full there.
        _SMOOTHING_ONLY_FAMILIES = ("maxcut", "spectral_dense")
        Q_eval = Q_base if family in _SMOOTHING_ONLY_FAMILIES else Q_full

        spec = analyze_spectrum(Q_eval)
        spec_row = {
            "eigen_min":        spec["eig_min"],
            "eigen_max":        spec["eig_max"],
            "condition_number": spec["condition_number"],
            "neg_eigs":         spec["negative_count"],
        }
        base_row = {
            "benchmark_family": family,
            "instance_id":      instance_id,
            "seed":             seed,
            "N":                N,
            "edge_prob":        meta.get("edge_prob"),
            "num_edges":        meta.get("num_edges"),
            "k_frac":           meta.get("k_frac"),
            "K_card":           meta.get("K_card"),
            "penalty_weight":   meta.get("penalty_weight"),
            "kappa_target":     meta.get("kappa_target"),
            "neg_frac":         meta.get("neg_frac"),
            **spec_row,
        }

        # N-proportional budget: scales with problem size so runtime stays
        # roughly constant per instance.  800,000 (insurance budget) is 16x
        # too large at N=50 — SA converges in ~10k steps and wastes 790k.
        # N * 2000 gives: N=50→100k, N=150→300k, N=300→600k.
        # This matches observed convergence thresholds for both families.
        instance_budget    = N * 2_000

        # SA: keep 20 restarts, but ensure EXACT total flip count equals instance_budget.
        # The previous integer division would drop remainders silently.
        num_starts = 20
        base_steps = instance_budget // num_starts
        rem_steps  = instance_budget %  num_starts
        # We pass a per-start steps value to run_sa_multistart; it uses constant steps_per_start.
        # To keep changes minimal and preserve existing SA code, we choose steps_per_start=base_steps
        # and accept that total may be slightly under budget when rem_steps>0.
        # However, we log budget_total_flips as instance_budget (allocated) and
        # target_flips_used as num_starts*base_steps (executed) for audit clarity.
        sa_steps_per_start = base_steps

        sa_executed_flips = num_starts * sa_steps_per_start  # <= instance_budget (exactly equal when rem_steps=0)

        # ---- SA ----
        sa_key = _key(family, instance_id, seed, N, "sa")
        sa_done = (sa_key in sa_done_keys)
        if skip_existing and sa_done:
            skipped += 1
            _bar_update(f"skip sa  | N={N} seed={seed}")
        else:
            _bar_update(f"running sa      N={N} seed={seed}")
            sa_res  = run_sa_multistart(
                Q_eval, N, seed,
                num_starts=num_starts,
                steps_per_start=sa_steps_per_start,
            )
            sa_eval = float(qubo_energy(Q_eval, sa_res["solution"]))
            sa_row  = {
                **base_row,
                "solver":        "sa",
                "raw_objective": sa_eval,
                "energy_total":  sa_eval,
                "is_feasible":   _check_feasible(sa_res["solution"], family, meta),
                "runtime_sec":   round(sa_res["runtime"], 3),

                # --- audit columns ---
                "budget_total_flips": int(instance_budget),       # allocated budget
                "target_flips_used":  int(sa_executed_flips),     # executed flips on target (SA uses target always)
                "echo_final_stage_flips": None,
                "echo_final_stage_frac":  None,
                "echo_tau0":              None,
                "echo_num_stages":        None,
                "echo_best_stage":        None,
            }
            _append_row(sa_csv, {c: sa_row.get(c) for c in _BASE_COLS})
            sa_done_keys.add(sa_key)  # keep resume set up to date
            completed_sa += 1
            _bar_update(f"done sa  {sa_eval:+.2f} | N={N} seed={seed}")

        # ---- ECHO-SA ----
        echo_key = _key(family, instance_id, seed, N, "sa_echo")
        echo_done = (echo_key in echo_done_keys)
        if skip_existing and echo_done:
            skipped += 1
            _bar_update(f"skip echo| N={N} seed={seed}")
        else:
            _bar_update(f"running echo-sa N={N} seed={seed}")
            # seed + 1_000_000: ECHO must explore different initial states than SA.
            # Same seed -> identical rng -> identical stage-0 restarts -> ties.
            echo_res  = run_echo_benchmark(
                Q_base, Q_pen, w_pen, N, seed + 1_000_000,
                params={
                    "total_budget": instance_budget,
                    "family":       family,
                    "K_card":       int(meta.get("K_card") or 0),
                    "echo_debug":   echo_debug,
                }
            )
            # Portfolio debug table
            if echo_debug and family == "portfolio_card" and "debug_rows" in echo_res:
                _print_portfolio_debug(echo_res, N, seed, instance_budget)

            # Re-evaluate on Q_eval (for spectral_dense: Q_base not Q_full)
            echo_eval = float(qubo_energy(Q_eval, echo_res["solution"]))

            # --- audit accounting for ECHO ---
            budgets = echo_res.get("budgets", [])
            final_flips = int(budgets[-1]) if budgets else 0
            final_frac  = float(final_flips) / float(max(instance_budget, 1))

            echo_row  = {
                **base_row,
                "solver":        "sa_echo",
                "raw_objective": echo_eval,
                "energy_total":  echo_eval,
                "is_feasible":   _check_feasible(echo_res["solution"], family, meta),
                "runtime_sec":   round(echo_res["runtime"], 3),

                # --- audit columns ---
                "budget_total_flips":      int(instance_budget),   # allocated continuation budget
                "target_flips_used":       int(final_flips),       # strict target-QUBO flips (final stage only)
                "echo_final_stage_flips":  int(final_flips),
                "echo_final_stage_frac":   float(final_frac),
                "echo_tau0":               float(echo_res.get("tau0", np.nan)),
                "echo_num_stages":         int(len(echo_res.get("stages", []))),
                "echo_best_stage":         int(echo_res.get("best_stage", -1)),
            }
            _append_row(echo_csv, {c: echo_row.get(c) for c in _BASE_COLS})
            echo_done_keys.add(echo_key)  # keep resume set up to date
            completed_echo += 1
            _bar_update(f"done echo{echo_eval:+.2f} | N={N} seed={seed}")

    if bar is not None:
        bar.close()

    print(f"\n  SA runs   : {completed_sa} new  ({skipped} skipped)")
    print(f"  ECHO runs : {completed_echo} new")
    print(f"  Building comparison table...")
    master = build_master(family, results_dir)
    if master is not None:
        _print_summary(family, master)


