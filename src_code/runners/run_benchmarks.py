# ---------------------------------------------------------------------------
# Pure SA for benchmark families  (M=K=0 — no one-hot blocks to project)
# ---------------------------------------------------------------------------

def _sa_pure_bitflip(
    Q: np.ndarray,
    N: int,
    rng: np.random.Generator,
    steps: int = 800_000,
    T0: float = 5.0,
    Tend: float = 0.01,
    initial_solution: Optional[np.ndarray] = None,
) -> tuple[np.ndarray, float]:
    """
    Single-run SA with pure bit-flip moves for unconstrained / single-constraint
    benchmark QUBOs where M=K=0 (no premium/deductible one-hot blocks).

    Uses the same budget and temperature schedule as the insurance SA.
    """
    v = (initial_solution.copy() if initial_solution is not None
         else rng.integers(0, 2, size=N).astype(float))
    e = qubo_energy(Q, v)
    best_v, best_e = v.copy(), e

    for t in range(steps):
        frac = t / max(1, steps - 1)
        T    = T0 * (1 - frac) + Tend * frac

        i     = int(rng.integers(0, N))
        v_new = v.copy()
        v_new[i] = 1.0 - v_new[i]
        e_new = qubo_energy(Q, v_new)

        if e_new < e or rng.random() < np.exp(-(e_new - e) / max(T, 1e-10)):
            v, e = v_new, e_new
        if e < best_e:
            best_e = e
            best_v = v.copy()

    return best_v, best_e


def run_sa_multistart(
    Q: np.ndarray,
    N: int,
    seed: int,
    num_starts: int = 20,
    steps_per_start: int = 40_000,
) -> Dict[str, Any]:
    """
    Multi-start SA matching the insurance baseline budget of 800,000 steps.
    20 starts × 40,000 steps = 800,000 total.
    """
    rng = np.random.default_rng(seed)
    t0  = time.time()

    best_v, best_e = None, float("inf")
    for _ in range(num_starts):
        v, e = _sa_pure_bitflip(Q, N, rng, steps=steps_per_start)
        if e < best_e:
            best_e = e
            best_v = v.copy()

    return {"solution": best_v, "energy": best_e, "runtime": time.time() - t0}


# ---------------------------------------------------------------------------
# SA-ECHO for benchmark families  (M=K=L=0, w_oh=w_reg=w_aff=0)
# ---------------------------------------------------------------------------

def run_echo_benchmark(
    Q_base: np.ndarray,
    Q_pen: np.ndarray,
    w_pen: float,
    N: int,
    seed: int,
    params: Optional[Dict] = None,
) -> Dict[str, Any]:
    """
    ECHO on a benchmark instance with M=K=L=0.

    Q_penalties = w_pen * Q_pen.
    Stage 0 and subsequent stages use _sa_pure_bitflip instead of the
    insurance SA (which requires M>0 and K>0 for one-hot projection).
    Matches the same 800,000-step budget as the insurance ECHO.
    """
    default_params = {
        "total_budget":         800_000,
        "max_stages":           8,
        "smoothing_percentile": 0.25,
        "base_beam":            20,
        "initial_restarts":     40,
        "T0":                   5.0,
        "Tend":                 0.01,
    }
    if params:
        default_params.update(params)
    p   = default_params
    rng = np.random.default_rng(seed)
    t0  = time.time()

    Q_penalties = w_pen * Q_pen

    tau0   = estimate_initial_smoothing(Q_base, Q_penalties, p["smoothing_percentile"])
    stages = compute_adaptive_stages(Q_base, Q_penalties, tau0,
                                     max_stages=p["max_stages"])
    budgets = allocate_budget_by_roughness(stages, Q_base, Q_penalties, tau0,
                                           p["total_budget"])

    spectra    = []
    candidates = []
    best_v, best_e = None, float("inf")
    best_stage = 0
    steps_used = 0

    for stage_idx, (t_val, budget, _) in enumerate(
        zip(stages, budgets, [None] * len(stages))
    ):
        tau_t = tau0 * (1 - t_val) ** 2
        Q_t   = Q_base + t_val * Q_penalties
        if tau_t > 1e-8:
            Q_t = Q_t + tau_t * np.eye(Q_t.shape[0])

        spec = analyze_spectrum(Q_t)
        spectra.append(spec)
        beam = adaptive_beam_size(spec["condition_number"], base_beam=p["base_beam"])

        if stage_idx == 0:
            n_restarts        = p["initial_restarts"]
            steps_per_restart = budget // n_restarts
            for _ in range(n_restarts):
                v, e = _sa_pure_bitflip(Q_t, N, rng,
                                        steps=steps_per_restart,
                                        T0=p["T0"], Tend=p["Tend"])
                candidates.append({"solution": v, "energy": e})
                if e < best_e:
                    best_e, best_v, best_stage = e, v.copy(), stage_idx
        else:
            steps_per_cand = budget // max(len(candidates), 1)
            new_cands = []
            for cand in candidates:
                v, e = _sa_pure_bitflip(Q_t, N, rng,
                                        steps=steps_per_cand,
                                        T0=p["T0"], Tend=p["Tend"],
                                        initial_solution=cand["solution"])
                new_cands.append({"solution": v, "energy": e})
                if e < best_e:
                    best_e, best_v, best_stage = e, v.copy(), stage_idx
            candidates = new_cands

        candidates.sort(key=lambda c: c["energy"])
        candidates = candidates[:beam]
        steps_used += budget

    # Evaluate best solution on the original unsmoothed Q
    Q_full  = Q_base + Q_penalties
    final_e = qubo_energy(Q_full, best_v)
    final_spec = spectra[-1] if spectra else {}

    return {
        "solution":    best_v,
        "energy":      final_e,
        "best_stage":  best_stage,
        "runtime":     time.time() - t0,
        "tau0":        tau0,
        "stages":      stages,
        "budgets":     budgets,
        "spectra":     spectra,
        "steps_used":  steps_used,
        "final_spec":  final_spec,
    }


# ---------------------------------------------------------------------------
# Feasibility check
# ---------------------------------------------------------------------------

def _check_feasible(solution: np.ndarray, family: str, meta: Dict) -> int:
    """
    Return 1 if solution satisfies the family constraint, 0 otherwise.

    maxcut        : unconstrained — always feasible.
    portfolio_card: exactly K_card assets must be selected.
    """
    if family == "maxcut":
        return 1
    elif family == "portfolio_card":
        K_card = int(meta["K_card"])
        selected = int(np.sum(solution > 0.5))
        return int(selected == K_card)
    return 0


# ---------------------------------------------------------------------------
# Skip / resume logic
# ---------------------------------------------------------------------------

def _already_computed(csv_path: Path, family: str, instance_id: str,
                       seed: int, N: int, solver: str) -> bool:
    """
    Return True if a row matching (family, instance_id, seed, N, solver)
    is already present in csv_path.  Returns False if the file doesn't exist.
    """
    if not csv_path.exists():
        return False
    try:
        df = pd.read_csv(csv_path)
        mask = (
            (df["benchmark_family"] == family)
            & (df["instance_id"]      == instance_id)
            & (df["seed"]             == seed)
            & (df["N"]                == N)
            & (df["solver"]           == solver)
        )
        return bool(mask.any())
    except Exception:
        return False


def _append_row(csv_path: Path, row: Dict[str, Any]) -> None:
    """Append one result row to csv_path; create with header if needed."""
    csv_path.parent.mkdir(parents=True, exist_ok=True)
    df_new = pd.DataFrame([row])
    if csv_path.exists():
        df_new.to_csv(csv_path, mode="a", header=False, index=False)
    else:
        df_new.to_csv(csv_path, index=False)


# ---------------------------------------------------------------------------
# Single instance runner
# ---------------------------------------------------------------------------

def _run_instance(
    family: str,
    N: int,
    seed: int,
    gen_kwargs: Dict[str, Any],
    sa_csv: Path,
    echo_csv: Path,
    skip_existing: bool,
    verbose: bool,
) -> None:
    instance_id = f"{family}_N{N}_seed{seed}"

    sa_done   = _already_computed(sa_csv,   family, instance_id, seed, N, "sa")
    echo_done = _already_computed(echo_csv, family, instance_id, seed, N, "sa_echo")

    if skip_existing and sa_done and echo_done:
        if verbose:
            print(f"  [skip] {instance_id}  (both sa + sa_echo already present)")
        return

    # Generate QUBO
    inst     = generate_instance(family, N, seed, **gen_kwargs)
    Q_base   = inst["Q_base"]
    Q_pen    = inst["Q_pen"]
    w_pen    = inst["w_pen"]
    meta     = inst["meta"]
    Q_full   = Q_base + w_pen * Q_pen

    # Spectral diagnostics of the final Q
    spec = analyze_spectrum(Q_full)
    spec_row = {
        "eigen_min":        spec["eig_min"],
        "eigen_max":        spec["eig_max"],
        "condition_number": spec["condition_number"],
        "neg_eigs":         spec["negative_count"],
    }

    base_row = {
        "benchmark_family": family,
        "instance_id":      instance_id,
        "seed":             seed,
        "N":                N,
        "edge_prob":        meta.get("edge_prob"),
        "num_edges":        meta.get("num_edges"),
        "k_frac":           meta.get("k_frac"),
        "K_card":           meta.get("K_card"),
        "penalty_weight":   meta.get("penalty_weight"),
        **spec_row,
    }

    # ------------------------------------------------------------------
    # Run SA
    # ------------------------------------------------------------------
    if not (skip_existing and sa_done):
        if verbose:
            print(f"  [sa]       {instance_id}")
        sa_res  = run_sa_multistart(Q_full, N, seed)
        sa_eval = float(qubo_energy(Q_full, sa_res["solution"]))
        sa_row  = {
            **base_row,
            "solver":        "sa",
            "raw_objective": sa_eval,
            "energy_total":  sa_eval,
            "is_feasible":   _check_feasible(sa_res["solution"], family, meta),
            "runtime_sec":   sa_res["runtime"],
        }
        _append_row(sa_csv, {c: sa_row.get(c) for c in _BASE_COLS})
    else:
        if verbose:
            print(f"  [skip sa]  {instance_id}")

    # ------------------------------------------------------------------
    # Run SA-ECHO
    # ------------------------------------------------------------------
    if not (skip_existing and echo_done):
        if verbose:
            print(f"  [sa_echo]  {instance_id}")
        echo_res  = run_echo_benchmark(Q_base, Q_pen, w_pen, N, seed)
        echo_eval = float(echo_res["energy"])
        echo_row  = {
            **base_row,
            "solver":        "sa_echo",
            "raw_objective": echo_eval,
            "energy_total":  echo_eval,
            "is_feasible":   _check_feasible(echo_res["solution"], family, meta),
            "runtime_sec":   echo_res["runtime"],
        }
        _append_row(echo_csv, {c: echo_row.get(c) for c in _BASE_COLS})
    else:
        if verbose:
            print(f"  [skip echo]{instance_id}")


# ---------------------------------------------------------------------------
# Merge / master CSV builder
# ---------------------------------------------------------------------------

def build_master(family: str, results_dir: Path) -> Optional[pd.DataFrame]:
    """
    Join sa_results.csv and sa_echo_results.csv into results_master.csv.

    Adds gap_echo_to_sa, echo_beats_sa, echo_ties_sa, echo_loses_sa.
    Returns the master DataFrame, or None if source files are missing.
    """
    sa_csv   = results_dir / "sa_results.csv"
    echo_csv = results_dir / "sa_echo_results.csv"

    missing = []
    if not sa_csv.exists():
        missing.append(str(sa_csv.relative_to(_ROOT)))
    if not echo_csv.exists():
        missing.append(str(echo_csv.relative_to(_ROOT)))
    if missing:
        print(f"  [master] Cannot build — missing: {', '.join(missing)}")
        print(f"           Run Option 3 (or Option 1) to generate solver outputs first.")
        return None

    sa_df   = pd.read_csv(sa_csv)
    echo_df = pd.read_csv(echo_csv)

    key_cols = ["benchmark_family", "instance_id", "seed", "N"]
    merged   = sa_df[key_cols + ["raw_objective"]].rename(
                   columns={"raw_objective": "sa_raw_objective"})
    merged   = merged.merge(
                   echo_df[key_cols + ["raw_objective"]].rename(
                       columns={"raw_objective": "sa_echo_raw_objective"}),
                   on=key_cols, how="inner")

    merged["gap_echo_to_sa"]  = merged["sa_echo_raw_objective"] - merged["sa_raw_objective"]
    tol = 1e-6
    merged["echo_beats_sa"]   = (merged["gap_echo_to_sa"] < -tol).astype(int)
    merged["echo_ties_sa"]    = (merged["gap_echo_to_sa"].abs() <= tol).astype(int)
    merged["echo_loses_sa"]   = (merged["gap_echo_to_sa"] >  tol).astype(int)

    # Attach spectral and meta columns from echo output
    extra_cols = [c for c in _BASE_COLS
                  if c not in key_cols + ["solver", "raw_objective",
                                          "energy_total", "is_feasible", "runtime_sec"]]
    merged = merged.merge(echo_df[key_cols + extra_cols].drop_duplicates(key_cols),
                          on=key_cols, how="left")

    master_path = results_dir / "results_master.csv"
    merged.to_csv(master_path, index=False)
    print(f"  [master] Written: {master_path.relative_to(_ROOT)}"
          f"  ({len(merged)} rows)")
    return merged


# ---------------------------------------------------------------------------
# Print summary
# ---------------------------------------------------------------------------

def _print_summary(family: str, master_df: pd.DataFrame) -> None:
    n      = len(master_df)
    wins   = int(master_df["echo_beats_sa"].sum())
    ties   = int(master_df["echo_ties_sa"].sum())
    losses = int(master_df["echo_loses_sa"].sum())
    med    = master_df["gap_echo_to_sa"].median()
    print(f"\n  {family} — SA-ECHO vs SA  ({n} instances)")
    print(f"  Wins  {wins}/{n} ({100*wins//max(n,1)}%)   "
          f"Ties {ties}   Losses {losses}")
    print(f"  Median gap (raw, negative = ECHO better): {med:.4f}")


# ---------------------------------------------------------------------------
# Main pipeline for a single family
# ---------------------------------------------------------------------------

def run_family(
    family: str,
    N_list: List[int],
    seeds: List[int],
    skip_existing: bool,
    verbose: bool,
    gen_kwargs: Optional[Dict] = None,
) -> None:
    """
    Full SA + SA-ECHO benchmark pipeline for one family.
    Manages directory creation, dependency checks, and master CSV build.
    """
    gen_kwargs = gen_kwargs or FAMILY_GEN_KWARGS.get(family, {})
    results_dir = _ROOT / "results" / family
    sa_csv      = results_dir / "sa_results.csv"
    echo_csv    = results_dir / "sa_echo_results.csv"

    # Create output directories, informing the user
    if not results_dir.exists():
        results_dir.mkdir(parents=True, exist_ok=True)
        print(f"  Initialized results/{family}/ directory.")

    n_total = len(N_list) * len(seeds)
    print(f"\n{'='*60}")
    print(f"  Benchmark: {family}")
    print(f"  N values : {N_list}")
    print(f"  Seeds    : {seeds[0]}..{seeds[-1]}  ({len(seeds)} seeds)")
    print(f"  Instances: {n_total}")
    if skip_existing:
        print(f"  Mode     : skip existing (resume-safe)")
    print(f"{'='*60}")

    done = 0
    for N in N_list:
        for seed in seeds:
            _run_instance(family, N, seed, gen_kwargs,
                          sa_csv, echo_csv, skip_existing, verbose)
            done += 1
            if done % 20 == 0:
                print(f"  Progress: {done}/{n_total}")

    print(f"\n  Completed {done} instances for {family}.")
    print(f"  Building comparison table...")
    master = build_master(family, results_dir)
    if master is not None:
        _print_summary(family, master)


# ---------------------------------------------------------------------------
# CLI entry point
# ---------------------------------------------------------------------------

def main() -> None:
    ap = argparse.ArgumentParser(
        description="Run SA and SA-ECHO benchmarks for Max-Cut and Portfolio."
    )
    ap.add_argument("--family", type=str, default="all",
                    choices=["maxcut", "portfolio_card", "all"],
                    help="Which benchmark family to run (default: all).")
    ap.add_argument("--N_list", type=str, default=None,
                    help="Comma-separated N values (default: 20,30,40,50).")
    ap.add_argument("--seeds", type=str, default=None,
                    help="Seed range 'start-end' or comma list (default: 1000-1019).")
    ap.add_argument("--edge_prob", type=float, default=0.5,
                    help="Max-Cut edge probability (default: 0.5).")
    ap.add_argument("--k_frac", type=float, default=0.3,
                    help="Portfolio cardinality fraction K/N (default: 0.3).")
    ap.add_argument("--verbose", action="store_true",
                    help="Print per-instance progress.")
    ap.add_argument("--force_rerun", action="store_true",
                    help="Rerun all instances even if results already exist.")
    # Hidden developer flag — not shown in help or menu
    ap.add_argument("--dev_fast", action="store_true",
                    help=argparse.SUPPRESS)

    args = ap.parse_args()

    # skip_existing: True by default; False only if --force_rerun
    skip_existing = (not args.force_rerun)

    # --dev_fast forces skip_existing=True (belt-and-suspenders)
    if args.dev_fast:
        skip_existing = True

    # Parse N_list
    if args.N_list:
        N_list = [int(x.strip()) for x in args.N_list.split(",")]
    else:
        N_list = DEFAULT_N_LIST

    # Parse seeds
    if args.seeds:
        s = args.seeds.strip()
        if "-" in s:
            lo, hi = s.split("-", 1)
            seeds = list(range(int(lo), int(hi) + 1))
        else:
            seeds = [int(x.strip()) for x in s.split(",")]
    else:
        seeds = DEFAULT_SEEDS

    families_to_run = DEFAULT_FAMILIES if args.family == "all" else [args.family]

    for fam in families_to_run:
        gen_kw = {**FAMILY_GEN_KWARGS.get(fam, {})}
        if fam == "maxcut":
            gen_kw["edge_prob"] = args.edge_prob
        elif fam == "portfolio_card":
            gen_kw["k_frac"] = args.k_frac

        run_family(fam, N_list, seeds,
                   skip_existing=skip_existing,
                   verbose=args.verbose,
                   gen_kwargs=gen_kw)


if __name__ == "__main__":
    main()