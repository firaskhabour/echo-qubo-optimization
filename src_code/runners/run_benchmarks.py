def _archive_results(family: str) -> None:
    """
    If results/<family>/ already exists and contains any CSV files, rename it
    to results/<family>_YYYYMMDD_HHMMSS/ before the new run starts.

    This preserves every previous run without manual intervention.
    The new run then writes into a fresh results/<family>/ directory.
    """
    results_dir = _ROOT / "results" / family
    if not results_dir.exists():
        return  # nothing to archive

    # Only archive if there is actual data inside
    csv_files = list(results_dir.glob("*.csv"))
    if not csv_files:
        return

    timestamp  = datetime.now().strftime("%Y%m%d_%H%M%S")
    archive_dir = _ROOT / "results" / f"{family}_{timestamp}"
    shutil.copytree(results_dir, archive_dir)
    shutil.rmtree(results_dir)
    print(f"  [archive] Existing results archived to: results/{family}_{timestamp}/")



# ---------------------------------------------------------------------------
# Main pipeline for a single family
# ---------------------------------------------------------------------------

def run_family(
    family: str,
    N_list: List[int],
    seeds: List[int],
    skip_existing: bool,
    verbose: bool,
    gen_kwargs: Optional[Dict] = None,
) -> None:
    """
    Full SA + SA-ECHO benchmark pipeline for one family.
    Progress bar advances after every individual solver run (SA or ECHO),
    so each step is visible and the bar never appears frozen.
    CSV files are written immediately after each solver completes so
    results can be monitored in real time.
    """
    gen_kwargs  = gen_kwargs or FAMILY_GEN_KWARGS.get(family, {})

    # Archive any existing results before starting a fresh run
    _archive_results(family)

    results_dir = _ROOT / "results" / family
    sa_csv      = results_dir / "sa_results.csv"
    echo_csv    = results_dir / "sa_echo_results.csv"

    if not results_dir.exists():
        results_dir.mkdir(parents=True, exist_ok=True)
        print(f"  Initialized results/{family}/ directory.")

    instances = [(N, seed) for N in N_list for seed in seeds]
    n_inst    = len(instances)
    # Each instance has 2 solver steps (SA + ECHO); bar advances on each
    n_steps   = n_inst * 2

    print(f"\n{'='*60}")
    print(f"  Benchmark : {family}")
    print(f"  N values  : {N_list}")
    print(f"  Seeds     : {seeds[0]}..{seeds[-1]}  ({len(seeds)} seeds)")
    print(f"  Instances : {n_inst}  (SA + ECHO-SA per instance = {n_steps} steps)")
    print(f"  Mode      : {'skip existing (resume-safe)' if skip_existing else 'force rerun'}")
    print(f"  Output    : results/{family}/sa_results.csv  (written after every SA)")
    print(f"              results/{family}/sa_echo_results.csv  (written after every ECHO-SA)")
    # Rough timing guidance so the user knows what to expect
    print(f"  Estimated : ~0.3-5s per step depending on N  "
          f"(N=50 ≈ 0.3s, N=300 ≈ 5s per solver)")
    print(f"{'='*60}", flush=True)

    # --- Pre-flight sanity checks (abort before wasting time if broken) ---
    try:
        _run_preflight(family, gen_kwargs)
    except PreflightError as exc:
        print(f"\n{'!'*60}")
        print(str(exc))
        print(f"{'!'*60}")
        print("  Aborting. Fix the issue and re-run.")
        return

    try:
        from tqdm import tqdm
        _tqdm = tqdm
    except ImportError:
        _tqdm = None

    completed_sa   = 0
    completed_echo = 0
    skipped        = 0

    if _tqdm is not None:
        bar = _tqdm(
            total=n_steps,
            desc=f"  {family}",
            unit="solver",
            ncols=72,
            bar_format=(
                "  {desc}: {percentage:3.0f}%|{bar}| "
                "{n_fmt}/{total_fmt} [{elapsed}<{remaining}]"
            ),
        )
    else:
        bar = None

    def _bar_update(label: str) -> None:
        if bar is not None:
            bar.set_postfix_str(label, refresh=True)
            bar.update(1)
        else:
            print(f"  {label}", flush=True)

    for N, seed in instances:
        instance_id = f"{family}_N{N}_seed{seed}"

        # ---- Generate QUBO (fast, no bar step) ----
        inst   = generate_instance(family, N, seed, **gen_kwargs)
        Q_base = inst["Q_base"]
        Q_pen  = inst["Q_pen"]
        w_pen  = inst["w_pen"]
        meta   = inst["meta"]
        Q_full = Q_base + w_pen * Q_pen

        spec = analyze_spectrum(Q_full)
        spec_row = {
            "eigen_min":        spec["eig_min"],
            "eigen_max":        spec["eig_max"],
            "condition_number": spec["condition_number"],
            "neg_eigs":         spec["negative_count"],
        }
        base_row = {
            "benchmark_family": family,
            "instance_id":      instance_id,
            "seed":             seed,
            "N":                N,
            "edge_prob":        meta.get("edge_prob"),
            "num_edges":        meta.get("num_edges"),
            "k_frac":           meta.get("k_frac"),
            "K_card":           meta.get("K_card"),
            "penalty_weight":   meta.get("penalty_weight"),
            **spec_row,
        }

        # N-proportional budget: scales with problem size so runtime stays
        # roughly constant per instance.  800,000 (insurance budget) is 16x
        # too large at N=50 — SA converges in ~10k steps and wastes 790k.
        # N * 2000 gives: N=50→100k, N=150→300k, N=300→600k.
        # This matches observed convergence thresholds for both families.
        instance_budget    = N * 2_000
        sa_steps_per_start = instance_budget // 20   # 20 restarts

        # ---- SA ----
        sa_done = _already_computed(sa_csv, family, instance_id, seed, N, "sa")
        if skip_existing and sa_done:
            skipped += 1
            _bar_update(f"skip sa  | N={N} seed={seed}")
        else:
            _bar_update(f"running sa      N={N} seed={seed}")
            sa_res  = run_sa_multistart(Q_full, N, seed,
                                        steps_per_start=sa_steps_per_start)
            sa_eval = float(qubo_energy(Q_full, sa_res["solution"]))
            sa_row  = {
                **base_row,
                "solver":        "sa",
                "raw_objective": sa_eval,
                "energy_total":  sa_eval,
                "is_feasible":   _check_feasible(sa_res["solution"], family, meta),
                "runtime_sec":   round(sa_res["runtime"], 3),
            }
            _append_row(sa_csv, {c: sa_row.get(c) for c in _BASE_COLS})
            completed_sa += 1
            _bar_update(f"done sa  {sa_eval:+.2f} | N={N} seed={seed}")

        # ---- ECHO-SA ----
        echo_done = _already_computed(echo_csv, family, instance_id, seed, N, "sa_echo")
        if skip_existing and echo_done:
            skipped += 1
            _bar_update(f"skip echo| N={N} seed={seed}")
        else:
            _bar_update(f"running echo-sa N={N} seed={seed}")
            # seed + 1_000_000: ECHO must explore different initial states than SA.
            # Same seed -> identical rng -> identical stage-0 restarts -> ties.
            echo_res  = run_echo_benchmark(Q_base, Q_pen, w_pen, N, seed + 1_000_000,
                                            params={"total_budget": instance_budget})
            echo_eval = float(echo_res["energy"])
            echo_row  = {
                **base_row,
                "solver":        "sa_echo",
                "raw_objective": echo_eval,
                "energy_total":  echo_eval,
                "is_feasible":   _check_feasible(echo_res["solution"], family, meta),
                "runtime_sec":   round(echo_res["runtime"], 3),
            }
            _append_row(echo_csv, {c: echo_row.get(c) for c in _BASE_COLS})
            completed_echo += 1
            _bar_update(f"done echo{echo_eval:+.2f} | N={N} seed={seed}")

    if bar is not None:
        bar.close()

    print(f"\n  SA runs   : {completed_sa} new  ({skipped} skipped)")
    print(f"  ECHO runs : {completed_echo} new")
    print(f"  Building comparison table...")
    master = build_master(family, results_dir)
    if master is not None:
        _print_summary(family, master)


# ---------------------------------------------------------------------------
# CLI entry point
# ---------------------------------------------------------------------------

def main() -> None:
    ap = argparse.ArgumentParser(
        description="Run SA and SA-ECHO benchmarks for Max-Cut and Portfolio."
    )
    ap.add_argument("--family", type=str, default="all",
                    choices=["maxcut", "portfolio_card", "all"],
                    help="Which benchmark family to run (default: all).")
    ap.add_argument("--N_list", type=str, default=None,
                    help="Comma-separated N values (default: 20,30,40,50).")
    ap.add_argument("--seeds", type=str, default=None,
                    help="Seed range 'start-end' or comma list (default: 1000-1019).")
    ap.add_argument("--edge_prob", type=float, default=0.5,
                    help="Max-Cut edge probability (default: 0.5).")
    ap.add_argument("--k_frac", type=float, default=0.15,
                    help="Portfolio cardinality fraction K/N (default: 0.15).")
    ap.add_argument("--verbose", action="store_true",
                    help="Print per-instance progress.")
    ap.add_argument("--force_rerun", action="store_true",
                    help="Rerun all instances even if results already exist.")
    # Hidden developer flag — not shown in help or menu
    ap.add_argument("--dev_fast", action="store_true",
                    help=argparse.SUPPRESS)

    args = ap.parse_args()

    # skip_existing: True by default; False only if --force_rerun
    skip_existing = (not args.force_rerun)

    # --dev_fast forces skip_existing=True (belt-and-suspenders)
    if args.dev_fast:
        skip_existing = True

    # Parse N_list
    if args.N_list:
        N_list = [int(x.strip()) for x in args.N_list.split(",")]
    else:
        N_list = DEFAULT_N_LIST

    # Parse seeds
    if args.seeds:
        s = args.seeds.strip()
        if "-" in s:
            lo, hi = s.split("-", 1)
            seeds = list(range(int(lo), int(hi) + 1))
        else:
            seeds = [int(x.strip()) for x in s.split(",")]
    else:
        seeds = DEFAULT_SEEDS

    families_to_run = DEFAULT_FAMILIES if args.family == "all" else [args.family]

    for fam in families_to_run:
        gen_kw = {**FAMILY_GEN_KWARGS.get(fam, {})}
        if fam == "maxcut":
            gen_kw["edge_prob"] = args.edge_prob
        elif fam == "portfolio_card":
            gen_kw["k_frac"] = args.k_frac

        run_family(fam, N_list, seeds,
                   skip_existing=skip_existing,
                   verbose=args.verbose,
                   gen_kwargs=gen_kw)


if __name__ == "__main__":
    main()