# Automatic temperature scaling
# ---------------------------------------------------------------------------

def _auto_temperature(Q: np.ndarray, n_samples: int = 200,
                       seed: int = 42) -> tuple[float, float]:
    """
    Derive T0 and Tend by directly measuring the mean absolute single-flip
    energy change on Q.

    Method: sample n_samples random (v, flip-at-i) pairs, compute |dE| for
    each, and set T0 = mean|dE|.  This is exact by definition — ratio
    T0/mean|dE| = 1.0 — and requires no structural assumptions about Q.

    At T0 = mean|dE|:
      P(accept uphill | dE = mean_dE) = exp(-1) ≈ 37%
      Expected total acceptance ≈ 68%  (good exploration without random walk)

    Previous formula (mean absolute row sum) overcounted for sparse QUBOs:
      maxcut N=50:  row-sum T0=22.95 vs actual mean|dE|=3.70  (ratio 6.2x too hot)
      portfolio N=50: row-sum T0=3051 vs actual mean|dE|=1595  (ratio 1.9x)

    Tend = T0 * 1e-3  (cool to 0.1% of T0 over the full schedule).
    Clamped to T0 >= 1.0 so Tend never underflows to zero.
    """
    N   = Q.shape[0]
    rng = np.random.default_rng(seed)
    des = []
    for _ in range(n_samples):
        v  = rng.integers(0, 2, size=N).astype(float)
        i  = int(rng.integers(0, N))
        vn = v.copy(); vn[i] = 1.0 - vn[i]
        de = abs(float(vn @ Q @ vn) - float(v @ Q @ v))
        des.append(de)
    T0   = max(1.0, float(np.mean(des)))
    Tend = T0 * 1e-3
    return T0, Tend



# ---------------------------------------------------------------------------
# Pure SA for benchmark families  (M=K=0 -- no one-hot blocks to project)
# ---------------------------------------------------------------------------

def _sa_pure_bitflip(
    Q: np.ndarray,
    N: int,
    rng: np.random.Generator,
    steps: int = 800_000,
    T0: Optional[float] = None,
    Tend: Optional[float] = None,
    initial_solution: Optional[np.ndarray] = None,
) -> tuple[np.ndarray, float]:
    """
    Single-run SA with pure bit-flip moves for unconstrained / single-constraint
    benchmark QUBOs where M=K=0 (no premium/deductible one-hot blocks).

    T0 and Tend default to None -> automatically derived from Q via
    _auto_temperature(Q). Pass explicit values to override.
    """
    if T0 is None or Tend is None:
        _T0, _Tend = _auto_temperature(Q)
        T0   = T0   if T0   is not None else _T0
        Tend = Tend if Tend is not None else _Tend

    v = (initial_solution.copy() if initial_solution is not None
         else rng.integers(0, 2, size=N).astype(float))
    e = qubo_energy(Q, v)
    best_v, best_e = v.copy(), e

    for t in range(steps):
        frac = t / max(1, steps - 1)
        T    = T0 * (1 - frac) + Tend * frac

        i     = int(rng.integers(0, N))
        v_new = v.copy()
        v_new[i] = 1.0 - v_new[i]
        e_new = qubo_energy(Q, v_new)

        if e_new < e or rng.random() < np.exp(-(e_new - e) / max(T, 1e-10)):
            v, e = v_new, e_new
        if e < best_e:
            best_e = e
            best_v = v.copy()

    return best_v, best_e


def run_sa_multistart(
    Q: np.ndarray,
    N: int,
    seed: int,
    num_starts: int = 20,
    steps_per_start: int = 40_000,
) -> Dict[str, Any]:
    """
    Multi-start SA matching the insurance baseline budget of 800,000 steps.
    20 starts x 40,000 steps = 800,000 total.

    Temperature is derived automatically from Q's energy scale so that both
    insurance QUBOs (energies O(1)) and portfolio QUBOs (energies O(w_pen))
    are explored correctly without manual tuning.
    """
    rng      = np.random.default_rng(seed)
    T0, Tend = _auto_temperature(Q)
    t0       = time.time()

    best_v, best_e = None, float("inf")
    for _ in range(num_starts):
        v, e = _sa_pure_bitflip(Q, N, rng, steps=steps_per_start, T0=T0, Tend=Tend)
        if e < best_e:
            best_e = e
            best_v = v.copy()

    return {"solution": best_v, "energy": best_e, "runtime": time.time() - t0}


# ---------------------------------------------------------------------------
# SA-ECHO for benchmark families  (M=K=L=0, w_oh=w_reg=w_aff=0)
# ---------------------------------------------------------------------------

def run_echo_benchmark(
    Q_base: np.ndarray,
    Q_pen: np.ndarray,
    w_pen: float,
    N: int,
    seed: int,
    params: Optional[Dict] = None,
) -> Dict[str, Any]:
    """
    ECHO on a benchmark instance with M=K=L=0.

    Q_penalties = w_pen * Q_pen.
    Stage 0 and subsequent stages use _sa_pure_bitflip instead of the
    insurance SA (which requires M>0 and K>0 for one-hot projection).
    Matches the same 800,000-step budget as the insurance ECHO.
    """
    # Auto-scale temperatures to Q_full's energy magnitude.
    # Q_full = Q_base + w_pen*Q_pen; used only for temperature estimation.
    Q_full_temp = Q_base + w_pen * Q_pen
    T0_auto, Tend_auto = _auto_temperature(Q_full_temp)

    default_params = {
        "total_budget":         800_000,
        "max_stages":           8,
        "smoothing_percentile": 0.25,
        "base_beam":            20,
        "initial_restarts":     40,
        "T0":                   T0_auto,
        "Tend":                 Tend_auto,
    }
    if params:
        default_params.update(params)
    p   = default_params
    rng = np.random.default_rng(seed)
    t0  = time.time()

    Q_penalties = w_pen * Q_pen

    tau0   = estimate_initial_smoothing(Q_base, Q_penalties, p["smoothing_percentile"])
    stages = compute_adaptive_stages(Q_base, Q_penalties, tau0,
                                     max_stages=p["max_stages"])
    budgets = allocate_budget_by_roughness(stages, Q_base, Q_penalties, tau0,
                                           p["total_budget"])

    spectra    = []
    candidates = []
    best_v, best_e = None, float("inf")
    best_stage = 0
    steps_used = 0

    for stage_idx, (t_val, budget, _) in enumerate(
        zip(stages, budgets, [None] * len(stages))
    ):
        tau_t = tau0 * (1 - t_val) ** 2
        Q_t   = Q_base + t_val * Q_penalties
        if tau_t > 1e-8:
            Q_t = Q_t + tau_t * np.eye(Q_t.shape[0])

        spec = analyze_spectrum(Q_t)
        spectra.append(spec)
        beam = adaptive_beam_size(spec["condition_number"], base_beam=p["base_beam"])

        if stage_idx == 0:
            n_restarts        = p["initial_restarts"]
            steps_per_restart = budget // n_restarts
            for _ in range(n_restarts):
                v, e = _sa_pure_bitflip(Q_t, N, rng,
                                        steps=steps_per_restart,
                                        T0=p["T0"], Tend=p["Tend"])
                candidates.append({"solution": v, "energy": e})
                if e < best_e:
                    best_e, best_v, best_stage = e, v.copy(), stage_idx
        else:
            steps_per_cand = budget // max(len(candidates), 1)
            new_cands = []
            for cand in candidates:
                v, e = _sa_pure_bitflip(Q_t, N, rng,
                                        steps=steps_per_cand,
                                        T0=p["T0"], Tend=p["Tend"],
                                        initial_solution=cand["solution"])
                new_cands.append({"solution": v, "energy": e})
                if e < best_e:
                    best_e, best_v, best_stage = e, v.copy(), stage_idx
            candidates = new_cands

        candidates.sort(key=lambda c: c["energy"])
        candidates = candidates[:beam]
        steps_used += budget

    # Evaluate best solution on the original unsmoothed Q
    Q_full  = Q_base + Q_penalties
    final_e = qubo_energy(Q_full, best_v)
    final_spec = spectra[-1] if spectra else {}

    return {
        "solution":    best_v,
        "energy":      final_e,
        "best_stage":  best_stage,
        "runtime":     time.time() - t0,
        "tau0":        tau0,
        "stages":      stages,
        "budgets":     budgets,
        "spectra":     spectra,
        "steps_used":  steps_used,
        "final_spec":  final_spec,
    }


