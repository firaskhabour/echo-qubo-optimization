def _check_feasible(solution: np.ndarray, family: str, meta: Dict) -> int:
    """
    Return 1 if solution satisfies the family constraint, 0 otherwise.

    maxcut        : unconstrained — always feasible.
    portfolio_card: exactly K_card assets must be selected.
    """
    if family == "maxcut":
        return 1
    elif family == "portfolio_card":
        K_card = int(meta["K_card"])
        selected = int(np.sum(solution > 0.5))
        return int(selected == K_card)
    return 0


