# ---------------------------------------------------------------------------
# Automatic temperature scaling
# ---------------------------------------------------------------------------

def _auto_temperature(Q: np.ndarray, n_samples: int = 200, 
                       seed: int = 42) -> tuple[float, float]:
    """
    Calibrate SA temperatures based on the QUBO's typical energy scale.
    
    RATIONALE:
    To ensure Simulated Annealing (SA) is scale-aware across different problem 
    sizes (N=50 to 300), we measure the 'typical' cost of a single bit-flip. 
    By setting T0 = mean(|ΔE|), we ensure that an 'average' uphill move is 
    accepted with probability P = exp(-1) ≈ 37% at the start.
    
    METHODOLOGY:
    1. Sample 'n_samples' random binary states 'v'.
    2. For each state, perform a random single-bit flip to state 'vn'.
    3. Calculate the absolute energy difference |E(vn) - E(v)|.
    4. T0 is the mean of these differences, ensuring the 'Ratio' diagnostic 
       (T0 / mean|ΔE|) equals 1.0 by definition.
    5. Tend is set to 0.1% of T0 to allow for final-stage exploitation.
    """
    N   = Q.shape[0]
    rng = np.random.default_rng(seed)
    des: List[float] = []

    for _ in range(int(n_samples)):
        v  = rng.integers(0, 2, size=N).astype(float)
        i  = int(rng.integers(0, N))
        vn = v.copy(); vn[i] = 1.0 - vn[i]
        # Energy calculation: v^T Q v
        de = abs(float(vn @ Q @ vn) - float(v @ Q @ v))
        des.append(de)

    # Statistical mean of energy scale
    T0 = float(np.mean(des)) if des else 1e-9

    # Numerical safety: prevent division by zero or underflow in exp(-dE/T)
    T0   = max(T0, 1e-9)
    Tend = max(T0 * 1e-3, 1e-12)

    return T0, Tend



# ---------------------------------------------------------------------------
# Pure SA for benchmark families  (M=K=0 -- no one-hot blocks to project)
# ---------------------------------------------------------------------------

def _sa_pure_bitflip(
    Q: np.ndarray,
    N: int,
    rng: np.random.Generator,
    steps: int = 800_000,
    T0: Optional[float] = None,
    Tend: Optional[float] = None,
    initial_solution: Optional[np.ndarray] = None,
) -> tuple[np.ndarray, float]:
    """
    Single-run SA with pure bit-flip moves for unconstrained / single-constraint
    benchmark QUBOs where M=K=0 (no premium/deductible one-hot blocks).

    T0 and Tend default to None -> automatically derived from Q via
    _auto_temperature(Q). Pass explicit values to override.
    """
    if T0 is None or Tend is None:
        _T0, _Tend = _auto_temperature(Q)
        T0   = T0   if T0   is not None else _T0
        Tend = Tend if Tend is not None else _Tend

    v = (initial_solution.copy() if initial_solution is not None
         else rng.integers(0, 2, size=N).astype(float))
    e = qubo_energy(Q, v)
    best_v, best_e = v.copy(), e

    for t in range(steps):
        frac = t / max(1, steps - 1)
        T    = T0 * (1 - frac) + Tend * frac

        i     = int(rng.integers(0, N))
        v_new = v.copy()
        v_new[i] = 1.0 - v_new[i]
        e_new = qubo_energy(Q, v_new)

        if e_new < e or rng.random() < np.exp(-(e_new - e) / max(T, 1e-10)):
            v, e = v_new, e_new
        if e < best_e:
            best_e = e
            best_v = v.copy()

    return best_v, best_e


def run_sa_multistart(
    Q: np.ndarray,
    N: int,
    seed: int,
    num_starts: int = 20,
    steps_per_start: int = 40_000,
) -> Dict[str, Any]:
    """
    Multi-start SA matching the insurance baseline budget of 800,000 steps.
    20 starts x 40,000 steps = 800,000 total.

    Temperature is derived automatically from Q's energy scale so that both
    insurance QUBOs (energies O(1)) and portfolio QUBOs (energies O(w_pen))
    are explored correctly without manual tuning.
    """
    rng      = np.random.default_rng(seed)
    T0, Tend = _auto_temperature(Q)
    t0       = time.time()

    best_v, best_e = None, float("inf")
    for _ in range(num_starts):
        v, e = _sa_pure_bitflip(Q, N, rng, steps=steps_per_start, T0=T0, Tend=Tend)
        if e < best_e:
            best_e = e
            best_v = v.copy()

    return {"solution": best_v, "energy": best_e, "runtime": time.time() - t0}


# ---------------------------------------------------------------------------
# Portfolio-specific ECHO helpers
# ---------------------------------------------------------------------------

#: Fixed homotopy t-values for portfolio_card.
#: Portfolio κ ≈ 2.33 and neg_eigs ≈ N−1 are constant across seeds and t,
#: so adaptive stage selection just tracks the τ-smoothing diagonal vanishing
#: and clusters stages near t≈0.30–0.40 — useless for the actual QUBO.
#: A hand-crafted, evenly-spaced schedule avoids this artifact entirely.
_PORTFOLIO_T_LIST: List[float] = [0.00, 0.15, 0.35, 0.60, 0.80, 1.00]

#: Minimum fraction of total ECHO budget guaranteed to the final stage (t=1).
#: Ensures ECHO evaluates the real target QUBO with enough flips to compete
#: with SA's 100% allocation (Audit A fix).
_PORTFOLIO_FINAL_FRAC_MIN: float = 0.40

#: Absolute minimum flips for the final stage regardless of budget size.
_PORTFOLIO_FINAL_ABS_MIN: int = 50_000

#: Per-stage budget cap for intermediate stages (t < 1) as fraction of total.
#: Prevents any single distorted intermediate stage consuming most of the budget
#: (Audit B fix — the t≈0.36 stage was taking 56% of budget).
_PORTFOLIO_INTER_STAGE_CAP: float = 0.15

#: MaxCut ECHO schedule constants
#: tau0 for maxcut is calibrated to |lambda_min(Q_base)|, not Q_pen spread.
#: Q_pen spread is O(50), which gives tau0=100 — 5.5× too large — erasing
#: all negative-eigenvalue signal and making stage-0 useless as a warm-up.
#: We compute the correct tau0 per-instance in _maxcut_stage_budgets.
_MAXCUT_FINAL_FRAC_MIN:  float = 0.70   # 70% of budget on Q_base directly
_MAXCUT_FINAL_ABS_MIN:   int   = 50_000  # absolute floor
_MAXCUT_WARMUP_RESTARTS: int   = 40      # restarts in the single warm-up stage


def _maxcut_stage_budgets(
    total_budget: int,
    tau0_actual: float,
) -> tuple[List[float], List[int]]:
    """
    Two-stage schedule for maxcut ECHO.

      Stage 0  (t=0): Q_base + tau0*I   — single warm-up, 30% of budget
      Stage 1  (t=1): Q_base            — refine on exact target, 70% of budget

    tau0 is computed per-instance as |lambda_min(Q_base)| so the smoothing
    just lifts Q_base to near-PSD — minimal distortion, maximal signal
    preservation.

    Returns ([0.0, 1.0], [B_warmup, B_final]).
    """
    B_final  = max(int(total_budget * _MAXCUT_FINAL_FRAC_MIN),
                   _MAXCUT_FINAL_ABS_MIN)
    B_final  = min(B_final, total_budget)
    B_warmup = total_budget - B_final
    B_warmup = max(B_warmup, 2_000)
    return [0.0, 1.0], [B_warmup, B_final]


#: K-repair interval: after how many SA steps to project back to K-cardinality
#: during intermediate stages.  Not applied at final stage (t=1) where the
#: full Q_full penalty already guides SA toward K naturally.
_PORTFOLIO_PROJ_INTERVAL: int = 500


def _portfolio_k_repair(
    v: np.ndarray,
    K: int,
    Q_stage: np.ndarray,
) -> np.ndarray:
    """
    Deterministic repair: flip variables one at a time until sum(v) == K.

    At each step, the variable whose flip costs least energy on Q_stage is
    chosen.  Operates in O(N × |sum(v)−K|) time.

    The repair targets K_card (the true cardinality), always.
    Returns a new array (does not mutate v).
    """
    v = v.copy()
    for _ in range(v.shape[0] + 1):          # bounded iterations
        sel = int(np.round(v.sum()))
        if sel == K:
            break
        e_cur = float(v @ Q_stage @ v)
        if sel > K:
            # turn off the on-variable whose removal costs least
            on_idx = np.where(v > 0.5)[0]
            best_j, best_de = -1, float("inf")
            for j in on_idx:
                vt = v.copy(); vt[j] = 0.0
                de = float(vt @ Q_stage @ vt) - e_cur
                if de < best_de:
                    best_de, best_j = de, j
            if best_j >= 0:
                v[best_j] = 0.0
        else:
            # turn on the off-variable whose addition costs least
            off_idx = np.where(v < 0.5)[0]
            best_j, best_de = -1, float("inf")
            for j in off_idx:
                vt = v.copy(); vt[j] = 1.0
                de = float(vt @ Q_stage @ vt) - e_cur
                if de < best_de:
                    best_de, best_j = de, j
            if best_j >= 0:
                v[best_j] = 1.0
    return v


def _portfolio_stage_budgets(
    total_budget: int,
) -> tuple[List[float], List[int]]:
    """
    Return (t_list, budgets) for portfolio_card ECHO.

    Budget rules (Audit A + B fixes):
      • Final stage (t=1) receives max(40% × total, 50k) flips.
      • Remaining flips split equally across the 5 intermediate stages.
      • Each intermediate stage is capped at 15% of total.
      • Any excess from capping is added to stage 0 (the τ-smoothed warm-up).

    Also enforces a minimum of 2,000 flips per stage so SA has something to do.
    """
    B_final = max(
        int(total_budget * _PORTFOLIO_FINAL_FRAC_MIN),
        _PORTFOLIO_FINAL_ABS_MIN,
    )
    B_final = min(B_final, total_budget)          # never exceed total
    B_inter  = total_budget - B_final

    inter_stages = _PORTFOLIO_T_LIST[:-1]         # all but t=1
    n_inter      = len(inter_stages)
    cap          = int(total_budget * _PORTFOLIO_INTER_STAGE_CAP)

    raw_each   = B_inter // n_inter
    inter_buds = [min(raw_each, cap) for _ in inter_stages]
    remainder  = B_inter - sum(inter_buds)        # from capping + int div
    inter_buds[0] += remainder                    # give surplus to warm-up

    # Enforce a minimum of 2,000 per intermediate stage
    for i in range(n_inter):
        inter_buds[i] = max(inter_buds[i], 2_000)

    budgets = inter_buds + [B_final]
    return _PORTFOLIO_T_LIST, budgets


def _sa_with_k_repair(
    Q_stage: np.ndarray,
    Q_full: np.ndarray,
    K: int,
    N: int,
    rng: np.random.Generator,
    steps: int,
    T0: float,
    Tend: float,
    v0: Optional[np.ndarray] = None,
    apply_repair: bool = True,
) -> tuple[np.ndarray, float]:
    """
    Run _sa_pure_bitflip on Q_stage, then apply K-repair if apply_repair=True.

    Returns (best_v_on_Q_full, energy_on_Q_full) so that all candidate
    comparisons are done on the actual target QUBO regardless of which stage
    the solution came from.
    """
    best_v, _ = _sa_pure_bitflip(Q_stage, N, rng, steps=steps, T0=T0, Tend=Tend,
                                  initial_solution=v0)
    if apply_repair:
        best_v = _portfolio_k_repair(best_v, K, Q_stage)
    return best_v, qubo_energy(Q_full, best_v)


# ---------------------------------------------------------------------------
# SA-ECHO for benchmark families  (M=K=L=0, w_oh=w_reg=w_aff=0)
# ---------------------------------------------------------------------------

def run_echo_benchmark(
    Q_base: np.ndarray,
    Q_pen: np.ndarray,
    w_pen: float,
    N: int,
    seed: int,
    params: Optional[Dict] = None,
) -> Dict[str, Any]:
    """
    ECHO on a benchmark instance with M=K=L=0.

    Q_penalties = w_pen * Q_pen.
    Stage 0 and subsequent stages use _sa_pure_bitflip instead of the
    insurance SA (which requires M>0 and K>0 for one-hot projection).
    Matches the same 800,000-step budget as the insurance ECHO.
    """
    # Auto-scale temperatures to Q_full's energy magnitude.
    # Q_full = Q_base + w_pen*Q_pen; used only for temperature estimation.
    Q_full_temp = Q_base + w_pen * Q_pen
    T0_auto, Tend_auto = _auto_temperature(Q_full_temp)

    default_params = {
        "total_budget":         800_000,
        "max_stages":           8,
        "smoothing_percentile": 0.25,
        "base_beam":            20,
        "initial_restarts":     40,
        "T0":                   T0_auto,
        "Tend":                 Tend_auto,
    }
    if params:
        default_params.update(params)
    p   = default_params
    rng = np.random.default_rng(seed)
    t0  = time.time()

    Q_penalties  = w_pen * Q_pen
    is_portfolio = p.get("family") == "portfolio_card"
    K_card       = p.get("K_card", 0)

    # For maxcut and spectral_dense, Q_pen is a smoothing matrix only — it is
    # added during ECHO stages to lift eigenvalues, but the real target QUBO is
    # Q_base alone.  Q_base + Q_pen is PSD, so v=0 trivially wins if used as
    # Q_full inside ECHO.  For portfolio_card, Q_pen IS the cardinality penalty
    # and must be included in Q_full.
    _family = p.get("family", "")
    if _family in ("maxcut", "spectral_dense"):
        Q_full = Q_base               # target is Q_base only
    else:
        Q_full = Q_base + Q_penalties  # portfolio: penalty is part of objective

    is_maxcut = (_family == "maxcut")

    if is_portfolio:
        # ── Portfolio-specific path (Audit A + B fix) ──────────────────────
        # Fixed t-schedule, guaranteed final-stage budget, K-repair.
        # Spectral diagnostics on Q_base only (no τ-smoothing distortion).
        stages, budgets = _portfolio_stage_budgets(p["total_budget"])
        tau0 = estimate_initial_smoothing(Q_base, Q_penalties,
                                          p["smoothing_percentile"])
        debug = p.get("echo_debug", False)
        debug_rows: List[Dict] = []
    elif is_maxcut:
        # ── MaxCut-specific path ────────────────────────────────────────────
        # Two-stage schedule: one τ-smoothed warm-up + one full final stage.
        # tau0 = |lambda_min(Q_base)|: lifts Q_base to just-PSD, preserving
        # all spectral signal. Using Q_pen spread (default) gives tau0=100 —
        # 5.5× too large — which erases negative-eigenvalue structure and makes
        # the warm-up useless (stage-0 candidates land at energy ≈ 0 on Q_base).
        eigs_base = np.linalg.eigvalsh(Q_base)
        tau0      = float(max(abs(eigs_base.min()), 1.0))  # |lambda_min|
        stages, budgets = _maxcut_stage_budgets(p["total_budget"], tau0)
        debug = False
    else:
        # ── Generic ECHO path (spectral_dense, …) ──────────────────────────
        tau0   = estimate_initial_smoothing(Q_base, Q_penalties,
                                            p["smoothing_percentile"])
        stages = compute_adaptive_stages(Q_base, Q_penalties, tau0,
                                         max_stages=p["max_stages"])
        budgets = allocate_budget_by_roughness(stages, Q_base, Q_penalties,
                                               tau0, p["total_budget"])
        debug = False

    spectra    = []
    candidates = []
    best_v, best_e = None, float("inf")
    best_stage = 0
    steps_used = 0

    # Families where Q_pen is a smoothing matrix only (maxcut, spectral_dense):
    # the homotopy is Q_t = Q_base + tau*(1-t)^2 * I
    # Q_pen is used only to calibrate tau0; it must NOT be blended in via t*Q_pen.
    # Blending it in makes Q_t PSD at every stage (including t=1), so v=0 wins.
    # Portfolio: Q_pen IS the penalty objective → normal homotopy Q_base + t*Q_pen.
    _is_smoothing_only = _family in ("maxcut", "spectral_dense")

    for stage_idx, (t_val, budget) in enumerate(zip(stages, budgets)):
        is_final = (t_val >= 1.0 - 1e-9)

        tau_t = tau0 * (1 - t_val) ** 2 if not is_final else 0.0

        if _is_smoothing_only:
            # Q_pen is NOT blended in — only the tau diagonal changes.
            # At t=0: Q_base + tau0*I (smooth surrogate)
            # At t=1: Q_base exactly (tau_t=0, no penalty term)
            Q_t    = Q_base.copy()
            Q_diag = Q_base          # diagnostics: always the real target
        else:
            # Portfolio (and any future constrained families):
            # penalty is part of the objective; blend it in via t.
            Q_t    = Q_base + t_val * Q_penalties
            Q_diag = Q_base + t_val * Q_penalties   # smoothing-free diagnostics

        if tau_t > 1e-8:
            Q_t = Q_t + tau_t * np.eye(Q_t.shape[0])
        spec   = analyze_spectrum(Q_diag)
        spectra.append(spec)
        beam = adaptive_beam_size(spec["condition_number"], base_beam=p["base_beam"])

        # Per-stage temperatures: derive T0/Tend from the actual Q_t being optimised.
        # Using the global T0 (calibrated to Q_base+Q_pen) over-heats smoothing-only
        # stages where Q_t has a much smaller energy scale, turning SA into a random
        # walk.  Portfolio uses a stable penalty scale so global T0 is fine there.
        if _is_smoothing_only:
            T0_stage, Tend_stage = _auto_temperature(Q_t)
        else:
            T0_stage, Tend_stage = p["T0"], p["Tend"]

        if stage_idx == 0:
            n_restarts        = p["initial_restarts"]
            steps_per_restart = budget // n_restarts

            if is_portfolio:
                for _ in range(n_restarts):
                    v, e_full = _sa_with_k_repair(
                        Q_t, Q_full, K_card, N, rng,
                        steps=steps_per_restart,
                        T0=p["T0"], Tend=p["Tend"],
                        apply_repair=True,
                    )
                    candidates.append({"solution": v, "energy": e_full})
                    if e_full < best_e:
                        best_e, best_v, best_stage = e_full, v.copy(), stage_idx
            else:
                for _ in range(n_restarts):
                    v, e = _sa_pure_bitflip(Q_t, N, rng,
                                            steps=steps_per_restart,
                                            T0=T0_stage, Tend=Tend_stage)
                    # Always rank candidates on Q_full (the real target QUBO).
                    # Q_t is PSD at stage 0 for maxcut/spectral_dense — ranking
                    # on Q_t makes v=0 win, corrupting all downstream stages.
                    e_target = qubo_energy(Q_full, v)
                    candidates.append({"solution": v, "energy": e_target})
                    if e_target < best_e:
                        best_e, best_v, best_stage = e_target, v.copy(), stage_idx
        else:
            steps_per_cand = budget // max(len(candidates), 1)
            new_cands = []

            if is_portfolio:
                for cand in candidates:
                    v, e_full = _sa_with_k_repair(
                        Q_t, Q_full, K_card, N, rng,
                        steps=steps_per_cand,
                        T0=p["T0"], Tend=p["Tend"],
                        v0=cand["solution"],
                        apply_repair=(not is_final),  # no repair at t=1
                    )
                    new_cands.append({"solution": v, "energy": e_full})
                    if e_full < best_e:
                        best_e, best_v, best_stage = e_full, v.copy(), stage_idx
            else:
                for cand in candidates:
                    v, e = _sa_pure_bitflip(Q_t, N, rng,
                                            steps=steps_per_cand,
                                            T0=T0_stage, Tend=Tend_stage,
                                            initial_solution=cand["solution"])
                    e_target = qubo_energy(Q_full, v)
                    new_cands.append({"solution": v, "energy": e_target})
                    if e_target < best_e:
                        best_e, best_v, best_stage = e_target, v.copy(), stage_idx
            candidates = new_cands

        candidates.sort(key=lambda c: c["energy"])
        candidates = candidates[:beam]
        steps_used += budget

        if debug and is_portfolio:
            best_cand = candidates[0] if candidates else {"solution": np.zeros(N), "energy": float("inf")}
            bv_dbg    = best_cand["solution"]
            k_sel     = int(np.round(bv_dbg.sum()))
            debug_rows.append({
                "stage_index":              stage_idx,
                "t":                        round(t_val, 4),
                "flips_allocated":          budget,
                "flips_used":               steps_per_restart * n_restarts if stage_idx == 0
                                            else steps_per_cand * len(candidates),
                "best_energy_stage":        round(best_cand["energy"], 4),
                "best_raw_objective_targetQ": round(qubo_energy(Q_full, bv_dbg), 4),
                "K_selected":               k_sel,
                "K_violation":              k_sel - K_card,
                "feasible_K":               int(k_sel == K_card),
            })

    final_e    = qubo_energy(Q_full, best_v) if best_v is not None else float("inf")
    final_spec = spectra[-1] if spectra else {}

    result = {
        "solution":    best_v,
        "energy":      final_e,
        "best_stage":  best_stage,
        "runtime":     time.time() - t0,
        "tau0":        tau0,
        "stages":      stages,
        "budgets":     budgets,
        "spectra":     spectra,
        "steps_used":  steps_used,
        "final_spec":  final_spec,
    }
    if debug and is_portfolio:
        result["debug_rows"]                = debug_rows
        result["final_stage_budget_frac"]   = budgets[-1] / max(p["total_budget"], 1)
        result["final_stage_flips"]         = budgets[-1]
    return result


