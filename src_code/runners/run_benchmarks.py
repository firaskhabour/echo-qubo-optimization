def main() -> None:
    ap = argparse.ArgumentParser(
        description="Run SA and SA-ECHO benchmarks for Max-Cut, Portfolio, and Spectral-Dense."
    )
    ap.add_argument("--family", type=str, default="all",
                    choices=["maxcut", "portfolio_card", "spectral_dense", "all"],
                    help="Which benchmark family to run (default: all).")
    ap.add_argument("--N_list", type=str, default=None,
                    help="Comma-separated N values (default: family-specific).")
    ap.add_argument("--seeds", type=str, default=None,
                    help="Seed range 'start-end' or comma list (default: 1000-1019).")
    ap.add_argument("--edge_prob", type=float, default=0.5,
                    help="Max-Cut edge probability (default: 0.5).")
    ap.add_argument("--k_frac", type=float, default=0.15,
                    help="Portfolio cardinality fraction K/N (default: 0.15).")
    ap.add_argument("--kappa", type=str, default=None,
                    help="Spectral-dense kappa_target value or comma-list "
                         "(default: 1e2,1e4,1e6). Single value also accepted.")
    ap.add_argument("--neg_frac", type=float, default=0.3,
                    help="Spectral-dense fraction of negative eigenvalues (default: 0.3).")
    ap.add_argument("--verbose", action="store_true",
                    help="Print per-instance progress.")
    ap.add_argument("--force_rerun", action="store_true",
                    help="Rerun all instances even if results already exist.")
    # Smoke-test mode: run one instance, print two CSV rows, exit.
    ap.add_argument("--smoke", action="store_true",
                    help="Smoke test: run exactly one instance, print CSV rows to stdout.")
    ap.add_argument("--N", type=int, default=None,
                    help="Single N value for --smoke mode.")
    ap.add_argument("--seed", type=int, default=None,
                    help="Single seed for --smoke mode.")
    ap.add_argument("--echo-debug", dest="echo_debug", action="store_true",
                    help="Print per-stage ECHO debug table for portfolio_card.")
    # Hidden developer flag — not shown in help or menu
    ap.add_argument("--dev_fast", action="store_true",
                    help=argparse.SUPPRESS)

    args = ap.parse_args()

    # ── Smoke-test mode ─────────────────────────────────────────────────────
    if args.smoke:
        _run_smoke_test(args)
        return

    # skip_existing: True by default; False only if --force_rerun
    skip_existing = (not args.force_rerun)
    if args.dev_fast:
        skip_existing = True

    # Parse N_list
    if args.N_list:
        N_list = [int(x.strip()) for x in args.N_list.split(",")]
    else:
        N_list = DEFAULT_N_LIST

    # Parse seeds
    if args.seeds:
        s = args.seeds.strip()
        if "-" in s:
            lo, hi = s.split("-", 1)
            seeds = list(range(int(lo), int(hi) + 1))
        else:
            seeds = [int(x.strip()) for x in s.split(",")]
    else:
        seeds = DEFAULT_SEEDS

    # Parse kappa_target_list for spectral_dense
    if args.kappa:
        kappa_list = [float(x.strip()) for x in args.kappa.split(",")]
    else:
        kappa_list = FAMILY_GEN_KWARGS["spectral_dense"]["kappa_target_list"]

    families_to_run = DEFAULT_FAMILIES if args.family == "all" else [args.family]

    for fam in families_to_run:
        gen_kw = {**FAMILY_GEN_KWARGS.get(fam, {})}
        if fam == "maxcut":
            gen_kw["edge_prob"] = args.edge_prob
        elif fam == "portfolio_card":
            gen_kw["k_frac"] = args.k_frac
        elif fam == "spectral_dense":
            # Run once per kappa_target value
            for kt in kappa_list:
                gen_kw_kt = {**gen_kw, "kappa_target": kt, "neg_frac": args.neg_frac}
                N_list_sd = [int(x.strip()) for x in args.N_list.split(",")
                             ] if args.N_list else [100, 200, 300]
                run_family(fam, N_list_sd, seeds,
                           skip_existing=skip_existing,
                           verbose=args.verbose,
                           gen_kwargs=gen_kw_kt,
                           echo_debug=False)
            continue

        run_family(fam, N_list, seeds,
                   skip_existing=skip_existing,
                   verbose=args.verbose,
                   gen_kwargs=gen_kw,
                   echo_debug=getattr(args, "echo_debug", False))


def _run_smoke_test(args: "argparse.Namespace") -> None:
    """
    Smoke-test mode: run SA and ECHO-SA on one spectral_dense instance and
    print exactly two CSV rows (one per solver) to stdout.

    Example invocation:
        python run_benchmarks.py --family spectral_dense --N 100 --seed 1000 \
                                 --kappa 1e4 --neg_frac 0.3 --smoke
    """
    family   = args.family if args.family != "all" else "spectral_dense"
    N        = args.N    if args.N    is not None else 100
    seed     = args.seed if args.seed is not None else 1000
    kappa    = float(args.kappa) if args.kappa else None   # None for non-spectral
    neg_frac = args.neg_frac if family == "spectral_dense" else None

    # Build gen_kw from the family defaults, not spectral_dense defaults
    gen_kw = {**FAMILY_GEN_KWARGS.get(family, {})}
    if family == "spectral_dense":
        gen_kw["kappa_target"] = kappa if kappa is not None else 1e4
        gen_kw["neg_frac"]     = neg_frac if neg_frac is not None else 0.3
    inst   = generate_instance(family, N, seed, **gen_kw)
    Q_base, Q_pen, w_pen = inst["Q_base"], inst["Q_pen"], inst["w_pen"]
    meta   = inst["meta"]
    Q_full = Q_base + w_pen * Q_pen

    instance_id = meta.get("instance_id") or f"{family}_N{N}_seed{seed}"
    # maxcut and spectral_dense: Q_pen is a smoothing matrix only.
    # Adding it to Q_eval makes the MaxCut Laplacian PSD → v=0 wins trivially.
    # portfolio_card: Q_pen IS the cardinality penalty, part of the objective.
    _SMOKE_SMOOTHING_ONLY = ("maxcut", "spectral_dense")
    Q_eval = Q_base if family in _SMOKE_SMOOTHING_ONLY else Q_full
    spec   = analyze_spectrum(Q_eval)   # spectrum of the actual evaluation QUBO
    budget = N * 2_000

    results = []
    for solver_name, run_fn in [
        ("sa",      lambda: run_sa_multistart(Q_eval, N, seed,
                                              steps_per_start=budget // 20)),
        ("sa_echo", lambda: run_echo_benchmark(
                            Q_base, Q_pen, w_pen, N,
                            seed + 1_000_000,
                            params={
                                "total_budget": budget,
                                "family":       family,
                                "K_card":       int(meta.get("K_card") or 0),
                                "echo_debug":   getattr(args, "echo_debug", False),
                            })),
    ]:
        res  = run_fn()
        if getattr(args, "echo_debug", False) and solver_name == "sa_echo" and "debug_rows" in res:
            _print_portfolio_debug(res, N, seed, budget)
        eval_energy = float(qubo_energy(Q_eval, res["solution"]))

        # Audit accounting for smoke mode
        if solver_name == "sa":
            budget_total_flips = int(budget)
            target_flips_used  = int((budget // 20) * 20)
            echo_final_stage_flips = None
            echo_final_stage_frac  = None
            echo_tau0              = None
            echo_num_stages        = None
            echo_best_stage        = None
        else:
            buds = res.get("budgets", [])
            final_flips = int(buds[-1]) if buds else 0
            budget_total_flips = int(budget)
            target_flips_used  = int(final_flips)
            echo_final_stage_flips = int(final_flips)
            echo_final_stage_frac  = float(final_flips) / float(max(budget, 1))
            echo_tau0              = float(res.get("tau0", np.nan))
            echo_num_stages        = int(len(res.get("stages", [])))
            echo_best_stage        = int(res.get("best_stage", -1))

        row  = {
            "benchmark_family": family,
            "instance_id":      instance_id,
            "seed":             seed,
            "N":                N,
            "solver":           solver_name,
            "raw_objective":    eval_energy,
            "energy_total":     eval_energy,
            "is_feasible":      _check_feasible(res["solution"], family, meta),
            "runtime_sec":      round(res["runtime"], 3),
            "eigen_min":        spec["eig_min"],
            "eigen_max":        spec["eig_max"],
            "condition_number": spec["condition_number"],
            "neg_eigs":         spec["negative_count"],
            "edge_prob":        meta.get("edge_prob"),
            "num_edges":        meta.get("num_edges"),
            "k_frac":           meta.get("k_frac"),
            "K_card":           meta.get("K_card"),
            "penalty_weight":   meta.get("penalty_weight", w_pen),
            "kappa_target":     meta.get("kappa_target"),
            "neg_frac":         meta.get("neg_frac"),

            # --- audit columns ---
            "budget_total_flips":      budget_total_flips,
            "target_flips_used":       target_flips_used,
            "echo_final_stage_flips":  echo_final_stage_flips,
            "echo_final_stage_frac":   echo_final_stage_frac,
            "echo_tau0":               echo_tau0,
            "echo_num_stages":         echo_num_stages,
            "echo_best_stage":         echo_best_stage,
        }
        results.append(row)

    import csv, sys as _sys
    cols = _BASE_COLS
    writer = csv.DictWriter(_sys.stdout, fieldnames=cols, extrasaction="ignore",
                            lineterminator="\n")
    writer.writeheader()
    for row in results:
        writer.writerow(row)


if __name__ == "__main__":
    main()